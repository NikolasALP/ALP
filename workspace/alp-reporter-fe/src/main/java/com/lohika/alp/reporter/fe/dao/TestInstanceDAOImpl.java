package com.lohika.alp.reporter.fe.dao;

import org.hibernate.SessionFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.orm.hibernate3.HibernateTemplate;
import org.springframework.stereotype.Repository;

import com.lohika.alp.reporter.db.model.TestInstance;

@Repository
public class TestInstanceDAOImpl implements TestInstanceDAO {
	
	private HibernateTemplate hibernateTemplate;

	@Autowired
	public void setSessionFactory(SessionFactory sessionFactory) {
		this.hibernateTemplate = new HibernateTemplate(sessionFactory);
	}

	@Override
	public TestInstance getTestInstance(long id) {
		return hibernateTemplate.get(TestInstance.class, id);
	}

}
